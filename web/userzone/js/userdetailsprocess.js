/* ------ function for education Field  --------  */

function addNewEduField()
{
    var newNode = document.createElement("textarea");
    newNode.classList.add("form-control");
    newNode.classList.add("eduField");
    newNode.classList.add("mt-2");
    newNode.setAttribute('placeholder', 'Enter here');

    var eduOb = document.getElementById("edu");
    var eduaddbuttonOb = document.getElementById("eduaddbutton");

    eduOb.insertBefore(newNode, eduaddbutton);
}

